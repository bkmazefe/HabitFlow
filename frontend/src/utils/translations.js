export const getTranslation = (language, key) => {
  const keys = key.split('.')
  let value = translations[language]
  
  for (const k of keys) {
    if (value && value[k]) {
      value = value[k]
    } else {
      // Fallback to English
      value = translations['en']
      for (const fallbackKey of keys) {
        if (value && value[fallbackKey]) {
          value = value[fallbackKey]
        } else {
          return key
        }
      }
      break
    }
  }
  
  return value
}
